void print_hex(const char *label, const unsigned char *data, size_t len)
{
    printf("%s: ", label);
    for (size_t i = 0; i < len; i++)
    {
        printf("%02x", data[i]);
    }
    printf("\n");
}

void test_aes128_ecb()
{
    unsigned char key[16] = {0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6, 0xab, 0xf7, 0x4d, 0x4a, 0x5e, 0x5b, 0x9b, 0x9b};
    unsigned char plaintext[16] = {0x32, 0x43, 0xf6, 0xa8, 0x88, 0x5a, 0x30, 0x8d, 0x31, 0x31, 0x98, 0xa2, 0xe0, 0x37, 0x07, 0x34};
    unsigned char ciphertext[16];
    aes128ctx ctx;

    aes128_ecb_keyexp(&ctx, key);
    aes128_ecb(ciphertext, plaintext, 1, &ctx);
    print_hex("AES-128 ECB ciphertext", ciphertext, 16);
    aes128_ctx_release(&ctx);
}

